    private int getNewImage(boolean nextEqTruePreviouseqFalse){
        int increment = (nextEqTruePreviouseqFalse)? 1 : (PickingNumberOPlayersFrame.maxNumberOfSupportedPlayers-1);
        for(int i = (this.selected+increment)%4; i != this.selected; i = (i+increment)%4){
            if(playersImagesAssociation.get(""+i) == null) {
                return (this.selected = i);
            }
        }
        return this.selected;
    }
   
