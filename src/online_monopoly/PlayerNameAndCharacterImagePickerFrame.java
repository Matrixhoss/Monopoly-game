    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        if(name!=null && !name.equals("")){
            for(String str : playersImagesAssociation.values()){
                if(str != null && name.equals(str)){
                    jLabel4.setText("Please enter a different name.");
                    return;
                }
            }
            playersImagesAssociation.put(this.selected+"", name);
            if(this.playersLeft == 0){
                new Mainboard_GUI_HD(0, 0, playersImagesAssociation);
            }
            else{
                new PlayerNameAndCharacterImagePickerFrame(images, playersImagesAssociation, playersLeft-1);
            }
            this.dispose();
        }else{
            jLabel4.setText("Please enter a valid name");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

